def get_fact(data, name):
    for item in data:
        if item["titel"].lower() == name.lower():
            if "bild" in item:               
                return item["fakta"], item["bild"]
            elif "namn" in item:
                return item["fakta"], item["namn"]
            elif "bild" in item and "namn" in item:
                return item["fakta"], item["bild"], item["namn"]
            return item["fakta"]
    return None

